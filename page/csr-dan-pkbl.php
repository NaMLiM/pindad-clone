            <p class="text-info">Pengelolaan Dana PKBL</p>

            <p>Jumlah aset dari PKBL pada tahun 2018 adalah sebesar 3.991 juta rupiah, dibandingkan dengan tahun 2017 yaitu sebesar 4.927 juta rupiah</p>

            <p>Pada tahun 2018, PKBL telah berhasil menyalurkan dana bantuan pinjaman kepada mitra binaan sebesar Rp 4.705.141.114, mengalami penurunan dibanding tahun lalu sebesar Rp 5.190.693.08.</p>

            <p>Realisasi penyaluran bantuan pinjaman mitra binaan pada tahun 2018 adalah sebesar Rp 1.790.640.232 dari sumber dana anggaran yaitu sebesar Rp 2.323.953.834.</p>


            <p class="text-info">Kinerja PKBL</p>

            <p>Kinerja PKBL dinilai berdasarkan dua indikator, yaitu efektivitas penyaluran dana program kemitraan dan kolektibilitas pengembalian dana PKBL. Adapun kedua kategori tersebut tertera sebagai berikut:</p>

            <p>1. Tingkat efektivitas penyaluran dana program kemitraan yang dilakukan oleh Perusahaan pada tahun 2018 adalah sebesar 94,42 % dengan skor 3, dibandingkan dengan pencapaian tahun 2017 sebesar 98.07% dengan skor 3</p>

            <p>2. Tingkat kolektibilitas pengembalian pinjaman dana program kemitraan pada tahun 2018 adalah sebesar 72.02% dengan skor 3, dibandingkan dengan <p>pencapaian tahun 2017 sebesar 90.56% dengan skor 3</p>


            <p class="text-info">Pengelolaan Lingkungan Hidup</p>

            <p>PT Pindad (Persero) berkomitmen tinggi dalam mengelola lingkungan hidup, salah satu bentuk komitmen ini ditunjukkan dengan diterbitkannya Skep No. Skep/41/P/BD/IX/2011 tanggal 26 September 2011 dan No. Skep/40/P/BD/IX/2011 tanggal 26 September 2011 tentang kebijakan energi. Komitmen ini juga diperlihatkan dengan diraihnya sertifikasi ISO 14001: 2004, penghargaan Industri Hijau Level 5 Terbaik dari Kementerian Perindustrian, dan penghargaan Proper Hijau dari Kementerian Lingkungan Hidup.</p>

