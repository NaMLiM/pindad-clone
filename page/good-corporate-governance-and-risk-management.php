            <p class="text-info">TATA KELOLA PERUSAHAAN</p>

            <p>Pedoman Tatakelola Perusahaan PT. Pindad (Persero) memiliki prinsip dasar yaitu :</p>

            <p>Transparansi, Akuntabilitas, Responsibilitas (pertanggung jawaban), Independensi (kemandirian) dan Fairness (kewajaran) untuk meningkatkan kinerja dan citra perusahaan.</p>

            <p>Penerapan Good Corporate Governance (GCG) pada PT Pindad (Persero) merupakan salah satu sarana untuk mencapai visi, misi dan tujuan perusahaan secara efektif dan lebih cepat, sehingga dapat memberikan kepercayaan kepada stakeholders bahwa perusahaan dikelola dengan baik dan benar untuk mendapatkan hasil yang wajar dan bernilai tinggi. Penerapan GCG juga mendorong pengelolaan perusahaan secara profesional, transparan dan efisien dengan pengelolaan risiko dan sumber daya yang lebih efektif serta untuk mendorong setiap unsur pimpinan dapat mengambil keputusan dan menjalankannya dengan landasan nilai moral yang tinggi dan kepatuhan pada peraturan perundang-undangan yang berlaku, serta kesadaran akan adanya tanggung jawab sosial perusahaan terhadap stakeholders dan mencegah penyimpangan pada proses bisnis perusahaan.</p>

            <p>Penjabaran dari prinsip-prinsip GCG tertuang dalam kode etik perilaku perusahaan dengan muatan ; Komitmen dengan stakedolders, Nilai-nilai Perusahaan, Benturan kepentingan, Pemberian hadiah dan donasi, Kepedulian K3LH, Kesempatan kerja dan promosi, Integritas laporan keuangan, Perlindungan informasi perusahaan, Perlindungan harta perusahaan, Kegiatan sosial politik, Etika dengan stakeholders, Mekanisme penegakan serta pelaporan atas pelanggaran berikut sanksi.</p>

            <p>Asesmen terhadap penerapan GCG dapat dilakukan oleh pihak eksternal (konsultan) dan juga oleh pihak internal perusahaan sendiri. Asesmen difokuskan pada capaian aktual kualitas penerapan GCG di perusahaan. Asesmen dilakukan dengan menggunakan instrument berupa Indikator yang dijabarkan ke dalam Parameter-parameter yang bersifat kuantitatif untuk masing-masing dilengkapi dengan Faktor Uji Kesesuaiannya.</p>

            <p>Pindad telah melaksanakan asesmen penerapan GCG sejak tahun 2004. Asesmen pertama dilaksanakan pada tanggal 7 September 2004 sampai dengan 3 Desember 2004 oleh Badan Pengawas Keuangan dan Pembangunan (BPKP) Perwakilan Provinsi Jawa Barat dengan skor 68,12% dan 22 rekomendasi dengan predikat kategori “Cukup”. Sebagaimana tercantum dalam RKAP tahun 2017, kegiatan asesmen GCG untuk tahun buku 2016 dilakukan secara mandiri (self assessment). Kegiatan ini telah dilaksanakan dengan baik oleh fungsi GCG Sesper Pindad pada bulan Desember 2017. Berdasarkan penilaian yang dilakukan, diperoleh predikat kategori “Baik” melalui capaian skor 84,036% serta 23 rekomendasi. Selanjutnya, penilaian yang dilakukan pada tahun 2018 untuk tahun buku 2017 85,128% dengan predikat “Sangat Baik”.</p>

            <p>Pengukuran penerapan GCG Pindad mengacu pada Peraturan Menteri Negara BUMN Nomor PER-01/ MBU/2011 tanggal 1 Agustus 2011 tentang Penerapan Tata Kelola Perusahaan yang Baik (Good Corporate Governance) pada Badan Usaha Milik Negara serta Surat Keputusan Direksi no. SKEP/35/P/BD/IX/2014 tanggal 30 September 2014 tentang Pedoman Penerapan GCG di lingkungan Pindad.</p>

