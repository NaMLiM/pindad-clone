            <div class="isi-judul">Perkembangan Usaha Perusahaan</div>

            <p>Hingga akhir tahun 2017, Pindad berhasil membukukan pertumbuhan yang positif dibandingkan dengan tahun sebelumnya, seiring dengan pertumbuhan perekonomian nasional yang juga mencatatkan tren positif.</p>

            <p>Kondisi ekonomi global pada tahun 2018 berdasarkan World Economic Outlook IMF bulan Juli 2017 diperkirakan lebih baik dari tahun-tahun sebelumnya dengan proyeksi pertumbuhan ekonomi global sebesar 3,6 persen, meningkat dari tahun 2017 sebesar 3,5 persen dan 2016 sebesar 3,2 persen. Perbaikan ekonomi global di tahun 2018 tersebut sejalan dengan permintaan global yang diproyeksikan meningkat dan harga komoditas diperkirakan cenderung stagnan.</p>

            <p>Volume perdagangan dunia sebagai dampak meningkatnya permintaan global diperkirakan akan tumbuh hingga 3,9 persen di tahun 2018. Pertumbuhan ekonomi global di tahun 2018 utamanya masih akan ditopang oleh kelompok negara berkembang yang mendapat dorongan dari perbaikan harga komoditas serta permintaan domestik yang kuat. Hal tersebut memberikan dampak pada pertumbuhan ekonomi di negara berkembang menjadi sebesar 4,8 persen, meningkat dibandingkan perkiraan tahun 2017 sebesar 4,6 persen.</p>

            <p>Tingkat permintaan domestik yang kuat tetap menjadi salah satu kekuatan utama ekonomi negara berkembang. Perbaikan harga komoditas serta permintaan global turut mendorong pertumbuhan di sisi eksternal. Berdasarkan perkembangan perekonomian dunia tersebut, serta didukung pembenahan internal danprogram-program pembangunan di dalam negeri, pertumbuhan ekonomi Indonesia dalam tahun 2018 diproyeksikan dapat mencapai 5,4 persen.</p>

            <p>Pertumbuhan ekonomi Indonesia diperkirakan akan semakin meningkat didukung dengan program pemerintah untuk terus melanjutkan kebijakan fiskal yang dapat mengoptimalkan pendapatan negara, dengan tetap menciptakan iklim investasi dan mendorong perkembangan dunia usaha, meningkatkan pelayanan kepada masyarakat, dan menjaga kelestarian lingkungan.</p>

            <div class="isi-judul">A.  Prospek Pertumbuhan Industri Pertahanan</div>

            <p>1. Kemampuan pertahanan dan keamanan saat ini dihadapkan pada situasi kurangnya jumlah dan ketidaksiapan Alutsista serta alat utama lainnya, apabila tidak dilakukan upaya percepatan penggantian, peningkatan, dan penguatan maka berpotensi menyulitkan penegakkan kedaulatan</p>

            <p>negara, penyelamatan bangsa, dan penjagaan keutuhan wilayah pada masa mendatang.</p>

            <p>2. Terjadinya kelemahan sistemik komponen cadangan dan pendukung pertahanan yang merupakan</p>

            <p>prasyarat berfungsinya sistem pertahanan semesta. Upaya lebih lanjut dalam pengembangan industri</p>

            <p>pertahanan nasional memerlukan dukungan berbagai kalangan agar dapat menciptakan kemandirian Alutsista TNI dan alat utama (Alut) Polri yang dibarengi dengan penataan lebih lanjut pola interaksi antara TNI dan Polri terkait dengan pelaksanaan tugas dan wewenangnya masingmasing.</p>

            <p>3. RPJMN 2015-2019 pada bidang pertahanan dan kemanan mengusung isu strategis yang selaras yaitu peningkatan kapasitas pertahanan dan stabilitas kemanan nasional. Isu strategis tersebut kemudian dijabarkan menjadi 7 (tujuh) sub isu strategis periode 5 (lima) tahun ke depan. Salah satu dari sub-isu tersebut yaitu permasalahan Alutsista TNI dan Almatsus Polri serta pemberdayaan industri pertahanan.</p>

            <p>4. Pemerintah Kabinet Kerja berkomitmen meningkatkan anggaran pertahanan menuju 1,5 persen dari PDB dalam kurun waktu 5 (lima) tahun.</p>

            <p>5. Perundangan yang berkaitan dengan sektorpengadaan alpalhankam telah disahkan yaitu UU No. 16 Tahun 2012 tentang industri Pertahanan. UU ini menjadi dasar pengembangan industri pertahanan untuk memenuhi kebutuhan alat peralatan TNI dan Polri.</p>

            <p>6. Dalam upaya memantapkan pemberdayaan industri pertahanan pada periode 2015-2019, KKIP menetapkan beberapa prioritas program nasional industri pertahanan, salah satunya yaitu: pembangunan industri propelan/mesiu, pengembangan roket nasional, pengembangan rudal nasional, pengembangan radar nasional, dan pengembangan tank sedang.</p>

            <p>7. Untuk mewujudkan pencapaian sasaran pembangunan bidang pertahanan dan kemanan tahun 2015-2019, maka strategi kebijakan pembangunan bidang Hankam yang akan dilakukan adalah strategi kebijakan pembangunan untuk mencapai sasaran terpenuhinya Alutsista TNI dan Almatsus Polri yang didukung industri pertahanan.</p>

