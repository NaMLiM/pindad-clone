            <p class="text-info">Program Kemitraan</p>

            <p>Program kemitraan adalah program untuk meningkatkan kemampuan usaha kecil agar menjadi tangguh dan mandiri melalui pemanfaatan dana perusahaan. Sasaran yang ingin dicapai dalam Program Kemitraan adalah meningkatkan kemampuan kewirausahaan dan manajerial serta memberikan bantuan permodalan, peningkatan kemampuan produksi, pemasaran, dan lain-lain sehingga usaha kecil yang dibina dapat menjadi usaha yang tangguh dan mandiri yang pada gilirannya nanti diharapkan dapat berkembang menjadi usaha menengah dan besar.</p>

            <p>Pada tahun 2018, Pindad telah menyalurkan dana bantuan pinjaman kepada mitra binaan sebesar Rp 4.705.141.114, mengalami penurunan dibanding tahun 2017 yaitu sebesar Rp 5.190.693.08.</p>


