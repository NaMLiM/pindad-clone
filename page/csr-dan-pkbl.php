            <p class="text-info">Penyelenggaraan Kegiatan Corporate Social Responsibility</p>

            <p>Program Tanggung Jawab Sosial Perusahaan atau Corporate Social Responsibility (CSR) merupakan investasi untuk mendukung terciptanya pembangunan berkelanjutan (sustainable development). Penerapan program CSR merupakan salah satu bentuk implementasi dari konsep tata kelola perusahaan yang baik (Good Corporate Governance). Oleh karena itu, diperlukan tata kelola perusahaan yang baik agar perilaku pelaku bisnis mempunyai arahan yang bisa dirujuk dengan mengatur hubungan seluruh kepentingan pemangku kepentingan (stakeholders) yang dapat dipenuhi secara proporsional. Pelaksanaan kegiatan CSR mengacu pada Surat Keputusan nomor SKEP/14/P/BD/X/2015, tanggal 19 Oktober 2015.</p>

            <p>PKBL merupakan kegiatan utama dari Program Tanggung Jawab Sosial Perusahaan (CSR/Corporate Social Responsibility). Selain PKBL, Program CSR Pindad meliputi Pengelolaan Lingkungan Hidup, K3, dan Tanggung Jawab kepada konsumen.</p>

            <p>Secara konsisten, PT Pindad (Persero) melaksanakan program CSR yang difokuskan pemberian donasi yang bertujuan untuk tujuan amal dan tujuan sosial dalam bentuk pemberian uang tunai atau barang, kepada pihak penerima donasi di luar Program Kemitraan dan Bina Lingkungan (PKBL) dan Community Development (Comdev). Para penerima donasi adalah masyarakat yang berada di sekitar perusahaan, kepanitiaan kegiatan organisasi pendidikan, Lembaga Swadaya Masyarakat (LSM), atau instansi yang bergerak di bidang amal/sosial.</p>

            <p>Untuk membangun dan memelihara rasa saling percaya antara pemangku jabatan dan masyarakat, secara sistematis PT Pindad (Persero) melaksanakan program CSR yang diimplementasikan melalui serangkaian kegiatan di beberapa bidang, yaitu :</p>

            <p>1.Kegiatan Seni Budaya</p>

            <p>2.Kegiatan Olahraga</p>

            <p>3.Kegiatan Khitanan Massal</p>

            <p>4.Kegiatan Pasar Murah</p>

