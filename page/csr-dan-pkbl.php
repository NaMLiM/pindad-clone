            <p class="text-info">Pengembangan Sosial dan Kemasyarakatan melalui PKBL</p>

            <p>PT Pindad (Persero) melakukan pengembangan sosial dan kemasyarakatan melalui kegiatan Program Kemitraan dan Bina Lingkungan (PKBL) dan program pengembangan masyarakat (Community Development atau Comdev). Kebijakan Program Kemitraan dan Bina Lingkungan (PKBL) PT Pindad (Persero) mengacu kepada Surat Keputusan Menteri BUMN Nomor PER-09/ MBU/2015 tentang Program Kemitraan dan Bina Lingkungan.</p>

            <p>Selain merujuk pada Kepmen tersebut, PT Pindad (Persero) juga merujuk pada prinsip ISO 26000. Pelaksanaan pengembangan sosial dan kemasyarakatan sesuai dengan arah kebijakan Perusahaan meliputi bantuan :</p>

