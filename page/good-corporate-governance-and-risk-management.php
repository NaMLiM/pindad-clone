            <p class="text-info">IMPLEMENTASI MANAJEMEN RISIKO TAHUN 2018</p>

            <p>Implementasi  atas Peraturan Menteri Negara Badan Usaha Milik Negara Nomor : Per-01/MBU/2011 tanggal 1 Agustus 2011, tentang penerapan tata kelola perusahaan yang baik (Good Corporate Governance) saat ini Pindad akan memasuki tahap manajemen risiko terintegrasi hal ini diindikasikan dengan setiap fungsi melakukan identifikasi, antisipasi dan secara efektif merespon beragam peristiwa yang dapat mempengaruhi kemampuan memenuhi strategis dan mencapai tujuan.</p>

            <p>Tujuan penerapan menajemen risiko adalah sebagai berikut :</p>

            <p>1.  Melindungi perusahaan dari risiko yang dapat merugikan dan menghambat pencapaian sasaran kinerja perusahaan</p>

            <p>2.  Membantu manajemen dalam menganalisis pelaksanaan kegiatan usaha agar memberikan nilai tambah bagi perusahaan dalam rangka membangun keunggulan operasional</p>

            <p>3.  Mendorong setiap pegawai perusahaan untuk bertindak hati-hati dalam menghadapi risiko perusahaan, sebagai upaya untuk memaksimalkan nilai perusahaan</p>

            <p>4.  Meningkatkan kepercayaan para pemangku kepentingan</p>

            <p>Dalam rangka memastikan pelaksanaan proses identifikasi, pengelolaan dan pemantauan risiko setiap Divisi fungsi manajemen risiko dan owner risk Divisi telah melaksanakan assessment manajemen risiko. Proses Assesment risiko merupakan proses untuk mengidentifikasi risiko-risiko apa saja yang mungkin terjadi di setiap unit/divisi, kemudian masing-masing risiko tersebut diberikan atribut sesuai dengan analisis yang dilakukan terhadap setiap risiko itu dengan menggunakan kriteria risiko yang ditentukan. Berikut adalah hasil assesment risiko tahun 2018 :</p>
            
            
