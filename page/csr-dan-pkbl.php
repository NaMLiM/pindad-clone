            <p>Pengelolaan Tanggung Jawab Perusahaan di Bidang Ketenagakerjaan, Kesehatan dan Keselamatan Kerja</p>

            <p>Komitmen Pindad terhadap kebijakan Mutu & K3LH tertuang dalam Skep No. Skep/22/P/BD/IX/2010 tanggal 25 September 2010.</p>

            <p>Perusahaan telah melakukan berbagai upaya untuk mewujudkan komitmen ini. Beberapa upaya itu antara lain Perusahaan:</p>

            <p>1. Memiliki dedikasi tinggi untuk menghasilkan produk dan menyediakan jasa yang konsisten dalam hal mutu, pengiriman tepat waktu, harga kompetitif, dan pelayanan terbaik.</p>

            <p>2. Menerapkan  dan mengembangkan Sistem Manajemen Mutu, Sistem Manajemen Kesehatan & Keselamatan Kerja, serta Sistem Manajemen Lingkungan Hidup secara benar, tepat, dan konsisten dengan komitmen mematuhi peraturan, perundangan, dan persyaratan mutu & K3LH yang berlaku, baik dari pelanggan, pemerintah maupun pihak terkait yang diikuti perusahaan.</p>

            <p>3. Berupaya mencegah kecelakaan kerja, penyakit akibat kerja, dan pencemaran lingkungan dengan menjamin setiap kegiatan/ aktivitas perusahaan berwawasan lingkungan dan tidak menimbulkan dampak negatif terhadap lingkungan hidup.</p>

            <p>4. Melakukan proses peningkatan yang berkelanjutan untuk meningkatkan kepuasan kepada pelanggan.</p>
