            <p>PT. PINDAD (Persero) sebagai salah satu dari 140 Badan Usaha Milik Negara (BUMN) sektor Industri Pengolahan, yang memproduksi alutsista TNI dan peralatan industri. Divisi Tempa dan Cor merupakan bagian dari bisnis PT. Pindad (Persero) yang bertujuan selain mendukung produksi unit usaha internal PT. Pindad (Persero) juga untuk memenuhi permintaan dan mendukung pasar lokal maupun ekspor dalam bidang jasa pengecoran logam dan jasa tempa, serta mendukung pembangunan infrastruktur perkeretaapian Indonesia melalui produk-produk prasarana kereta api.</p>
            <p>Produk-produk yang dihasilkan dari jasa pengecoran logam, jasa tempa dan produk prasarana kereta api diproduksi dengan mengedepankan kualitas berdasarkan standar internasional maupun nasional dan disertai jaminan mutu produk.</p>
            Produk
            <p>PT Pindad (Persero) mengawali bisnis dalam bidang Sarana dan Prasarana Kereta Api pada tahun 1983 dan mulai berproduksi pada tahun 1984 dengan memproduksi alat penambat rel tipe DE-Clips (DE-Clips Rail Fastener) yang merupakan lisensi dari Hollandia Kloos dan Ewem AG.</p>
            <p>Pada tanggal 23 September 1997 Penandatanganan Perjanjian Kerja Sama Antara PT. Pindad dan PT. KA yang diwakili oleh Dirut Pindad, Budi Santoso dan Dirut Perumka, Soemino Eko Saputro yang menghasilkan litbang untuk produk alat penambat rel tipe KA-Clip dan hak kepemilikan PT. Kereta Api Indonesia (Persero). PT. Pindad (Persero) patent no ID 0 007 930 pada 19 April 2000 dikeluarkanlah ijin penggunaan KA-Clip dari Dirjen Perkeretaapian Departemen Perhubungan untuk digunakan pada proyek dilingkungan Direktorat Jenderal Perkeretaapian maupun di wilayah kerja PT. Kereta api Indonesia (Persero)</p>
            <p>Pada Januari 2008 pengembangan dilakukan kembali dengan memproduksi alat penambat rel type e-Clip kemudian dilakukan test track dan pada 2010 ijin penggunaan pengesahan dan rekomendasi pemakaian dari Direktur Jenderal Perkeretaapian Kementeriam Perhubungan No. KA.405/SK.10/DJKA/II/10</p>
            <p>Alat Penambat Rel (Rail fastener) produksi PT Pindad (Persero) sangat kompetitif serta dengan jaminan kualitas pekerjaan yang tinggi dimana dalam proses produksi kami menggunakan Standar Iinternational maupun National seperti AREMA/AREA Standard, SNI Standar, serta didukung oleh sumber daya manusia yang telah berpengalaman dengan kemampuan dan keahlian yang sudah teruji.</p>
            <p>Kualitas hasil produksi yang bermutu tinggi dan kepuasan pelanggan dalam menggunakan produk yang kami hasilkan. merupakan tujuan bisnis Pindad. Dengan semangat dan keyakinan yang tinggi kami selalu siap bekerja keras untuk mengembangkan keahlian dan teknologi yang dimiliki agar dapat menawarkan solusi terbaik bagi pelanggan maupun pengguna produk prasarana dan sarana Kereta Api kami.</p>
